package com.ducat;

public class Ch_1_Literals {
    public static void main(String[] args) {
        int a=10;
        short b=20;
        char agg='b';
        long c=23445666666666666l;
        float d=12.23f;
        double e=12.455555;
        byte f=122;
        boolean g=true;
        String str="nitin";

        System.out.println("Integer number: "+a);
        System.out.println("Short number: "+b);
        System.out.println("Character char: "+agg);
        System.out.println("Long number: "+c);
        System.out.println("Float number: "+d);
        System.out.println("Double number: "+e);
        System.out.println("Byte number: "+f);
        System.out.println("Boolean : "+g);
        System.out.println("String: "+str);
    }
}
